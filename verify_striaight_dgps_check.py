def calculate_baseline_fit(
    trajectory,
    start_idx,
    end_idx,
    num_baseline_points=5,
    max_threshold=0.2,
    rms_threshold=0.1,
):
    """
    Implement the baseline fit algorithm.

    Args:
        trajectory: List of trajectory points with ego_x, ego_y
        start_idx: Start index of peak window
        end_idx: End index of peak window
        num_baseline_points: Number of points to use before/after peak
        max_threshold: Maximum perpendicular distance threshold (meters)
        rms_threshold: RMS distance threshold (meters)

    Returns:
        Dictionary with results
    """
    # Collect baseline points
    baseline_indices = []

    # Points before peak
    before_start = max(0, start_idx - num_baseline_points)
    num_before = 0
    for i in range(before_start, start_idx):
        baseline_indices.append(i)
        num_before += 1

    # If no points before, use start_idx itself
    if num_before == 0:
        baseline_indices.append(start_idx)
        num_before = 1

    # Points after peak
    after_end = min(len(trajectory) - 1, end_idx + num_baseline_points)
    num_after = 0
    for i in range(end_idx + 1, after_end + 1):
        baseline_indices.append(i)
        num_after += 1

    # If no points after, use end_idx itself
    if num_after == 0:
        baseline_indices.append(end_idx)
        num_after = 1

    if len(baseline_indices) < 2:
        return {
            "error": "Insufficient baseline points",
            "num_baseline_points": len(baseline_indices),
            "num_before": num_before,
            "num_after": num_after,
        }

    # Calculate centroid of baseline points
    sum_x = sum(trajectory[i]["ego_x"] for i in baseline_indices)
    sum_y = sum(trajectory[i]["ego_y"] for i in baseline_indices)
    centroid_x = sum_x / len(baseline_indices)
    centroid_y = sum_y / len(baseline_indices)

    # PCA on baseline points
    sum_xx = sum_xy = sum_yy = 0.0
    for idx in baseline_indices:
        dx = trajectory[idx]["ego_x"] - centroid_x
        dy = trajectory[idx]["ego_y"] - centroid_y
        sum_xx += dx * dx
        sum_xy += dx * dy
        sum_yy += dy * dy

    # Find principal direction
    trace = sum_xx + sum_yy
    det = sum_xx * sum_yy - sum_xy * sum_xy
    eigenvalue = 0.5 * (trace + math.sqrt(max(0.0, trace * trace - 4.0 * det)))

    dir_x = sum_xy
    dir_y = eigenvalue - sum_xx
    dir_norm = math.sqrt(dir_x * dir_x + dir_y * dir_y)

    if dir_norm < 1e-6:
        return {
            "error": "Degenerate baseline (all points identical)",
            "num_baseline_points": len(baseline_indices),
            "num_before": num_before,
            "num_after": num_after,
        }

    dir_x /= dir_norm
    dir_y /= dir_norm

    # Measure peak window deviations from baseline
    max_dist = 0.0
    sum_sq = 0.0

    for i in range(start_idx, end_idx + 1):
        px = trajectory[i]["ego_x"]
        py = trajectory[i]["ego_y"]
        dx = px - centroid_x
        dy = py - centroid_y
        perp = abs(dx * dir_y - dy * dir_x)
        max_dist = max(max_dist, perp)
        sum_sq += perp * perp

    num_peak_points = end_idx - start_idx + 1
    rms = math.sqrt(sum_sq / num_peak_points)

    # Check thresholds
    is_straight = (max_dist < max_threshold) and (rms < rms_threshold)

    return {
        "num_baseline_points": len(baseline_indices),
        "num_before": num_before,
        "num_after": num_after,
        "baseline_direction": (dir_x, dir_y),
        "baseline_angle_deg": math.degrees(math.atan2(dir_y, dir_x)),
        "max_distance_m": max_dist,
        "rms_distance_m": rms,
        "max_threshold_m": max_threshold,
        "rms_threshold_m": rms_threshold,
        "passes_max": max_dist < max_threshold,
        "passes_rms": rms < rms_threshold,
        "is_straight": is_straight,
        "num_peak_points": num_peak_points,
    }


def print_results(test_num, test_case, result, verbose=True):
    """Print formatted results."""
    trajectory = test_case["trajectory"]
    start_idx = test_case["start_idx"]
    end_idx = test_case["end_idx"]

    if verbose:
        print("=" * 70)
        print(f"TEST CASE {test_num}: {test_case['name']}")
        print("=" * 70)
        print(f"Description: {test_case['description']}")
        print(f"\nTrajectory info:")
        print(f"  Total points: {len(trajectory)}")
        print(
            f"  Peak window: indices [{start_idx}, {end_idx}] ({end_idx - start_idx + 1} points)"
        )
        print(
            f"  Start point: ({trajectory[start_idx]['ego_x']:.3f}, {trajectory[start_idx]['ego_y']:.3f})"
        )
        print(
            f"  End point: ({trajectory[end_idx]['ego_x']:.3f}, {trajectory[end_idx]['ego_y']:.3f})"
        )
        print(
            f"  Lateral displacement: {trajectory[end_idx]['ego_y'] - trajectory[start_idx]['ego_y']:.3f}m"
        )

    if result is None or "error" in result:
        error_msg = result.get("error", "Unknown error")
        if verbose:
            print(f"\nERROR: {error_msg}")
        return False

    if verbose:
        print(f"\nBaseline info:")
        print(f"  Total baseline points: {result['num_baseline_points']}")
        print(f"  Points before peak: {result['num_before']}")
        print(f"  Points after peak: {result['num_after']}")
        print(
            f"  Baseline direction: ({result['baseline_direction'][0]:.6f}, {result['baseline_direction'][1]:.6f})"
        )
        print(f"  Baseline angle: {result['baseline_angle_deg']:.3f}°")

        print(f"\nPeak deviations from baseline:")
        print(f"  Max distance: {result['max_distance_m']:.6f}m")
        print(f"  RMS distance: {result['rms_distance_m']:.6f}m")

        print(f"\nThreshold checks:")
        print(
            f"  Max < {result['max_threshold_m']:.2f}m? {result['passes_max']} ({result['max_distance_m']:.6f}m)"
        )
        print(
            f"  RMS < {result['rms_threshold_m']:.2f}m? {result['passes_rms']} ({result['rms_distance_m']:.6f}m)"
        )

        print(f"\n{'='*70}")

    actual = "STRAIGHT" if result["is_straight"] else "NOT_STRAIGHT"
    expected = test_case["expected"]
    passed = actual == expected

    if verbose:
        if result["is_straight"]:
            print("RESULT: Classified as STRAIGHT")
            print("=> Peak would be FILTERED OUT (lane reference change)")
        else:
            print("RESULT: Classified as NOT STRAIGHT")
            print("=> Peak would be KEPT (real maneuver)")

        print(f"\nExpected: {expected}")
        print(f"Actual: {actual}")
        if passed:
            print("✅ TEST PASSED")
        else:
            print("❌ TEST FAILED")
        print("=" * 70)
        print()

    return passed


def run_all_tests(max_threshold=0.2, rms_threshold=0.1, verbose=True):
    """Run all test cases."""
    if verbose:
        print("\n" + "=" * 70)
        print(f"RUNNING ALL TESTS")
        print(f"Thresholds: Max={max_threshold:.2f}m, RMS={rms_threshold:.2f}m")
        print("=" * 70)
        print()

    results = {}
    for test_num in sorted(TEST_CASES.keys()):
        test_case = TEST_CASES[test_num]
        result = calculate_baseline_fit(
            test_case["trajectory"],
            test_case["start_idx"],
            test_case["end_idx"],
            max_threshold=max_threshold,
            rms_threshold=rms_threshold,
        )
        passed = print_results(test_num, test_case, result, verbose)
        results[test_num] = {"passed": passed, "result": result, "test_case": test_case}

    # Summary
    print("\n" + "=" * 70)
    print("SUMMARY")
    print("=" * 70)
    total = len(results)
    passed_count = sum(1 for r in results.values() if r["passed"])

    for test_num in sorted(results.keys()):
        r = results[test_num]
        status = "✅ PASS" if r["passed"] else "❌ FAIL"
        max_d = r["result"]["max_distance_m"] if "max_distance_m" in r["result"] else 0
        rms_d = r["result"]["rms_distance_m"] if "rms_distance_m" in r["result"] else 0
        print(
            f"Test {test_num}: {status} | Max={max_d:.3f}m RMS={rms_d:.3f}m | {r['test_case']['name']}"
        )

    print(f"\nTotal: {passed_count}/{total} tests passed")
    print("=" * 70)

    return results


def main():
    """Main function for testing."""
    if len(sys.argv) < 2:
        print("Usage:")
        print("  python verify_baseline_fit.py all              # Run all test cases")
        print(
            "  python verify_baseline_fit.py <test_num>       # Run specific test case"
        )
        print(
            "  python verify_baseline_fit.py all 0.3 0.15     # Run all with custom thresholds"
        )
        print()
        print("Available test cases:")
        for num, tc in sorted(TEST_CASES.items()):
            print(f"  {num}: {tc['name']}")
        sys.exit(1)

    command = sys.argv[1]

    # Parse thresholds if provided
    max_threshold = float(sys.argv[2]) if len(sys.argv) > 2 else 0.2
    rms_threshold = float(sys.argv[3]) if len(sys.argv) > 3 else 0.1

    if command == "all":
        run_all_tests(max_threshold, rms_threshold)
    else:
        test_num = int(command)
        if test_num not in TEST_CASES:
            print(f"Error: Test case {test_num} not found")
            sys.exit(1)

        test_case = TEST_CASES[test_num]
        result = calculate_baseline_fit(
            test_case["trajectory"],
            test_case["start_idx"],
            test_case["end_idx"],
            max_threshold=max_threshold,
            rms_threshold=rms_threshold,
        )
        print_results(test_num, test_case, result, verbose=True)


if __name__ == "__main__":
    main()
